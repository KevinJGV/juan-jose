str1 = "Abril"
str2 = "Abrilinamico"

#Condicional Simple
if str1 < str2:
    print("La cadena 1 es menor que la cadena 2")
print("Fin de Codigo")

#Condicional Doble
if str1 < str2:
    print("Verdadero")
else:
    print("Falso")
print("Fin de Codigo")

#Condicional Multiple
if str1 == str2:
    print("Iguales")
elif str1 < str2:
    print("1 menor que 2")
else:
    print("2 menor que 1")