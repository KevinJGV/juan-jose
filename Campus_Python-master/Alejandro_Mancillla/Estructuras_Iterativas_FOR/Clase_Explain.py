#Ciclo FOR usando Range
for i in range(5):              #0, 1, 2, 3, 4,
    print(i, end=", ")          #end me indica el final que hace el print
print("\n")

for i in range(-1,5):           #-1, 0, 1, 2, 3, 4,
    print(i, end=", ")
print("\n")

for i in range(-1, 5, 2):       #-1, 1, 3,
    print(i, end=", ")          
print("\n")

for i in range(5, 1, -2):       #5, 3, 
    print(i, end=", ")
print("\n")    
